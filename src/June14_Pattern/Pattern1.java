package June14_Pattern;

// 1  2  3
// 1  2
// 1

public class Pattern1 {

	public static void main(String[] args) {
		
	for (int i=3;  i>0;  i--)
		{
			for(int j=1;   j<=i;  j++)
			{
				System.out.print(j+" ");       //print to print on same line
			}
			System.out.println();
		}//for loop ends
	
	
	}// main ends	
}
